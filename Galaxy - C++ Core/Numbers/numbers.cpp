#include<bits/stdc++.h>
using namespace std;

int main()
{
    float num1 = 4.23;
    double pi = acos(-1.0);

    cout<<"ceil function: "<<ceil(num1)<<endl;
    cout<<"floor function: "<<floor(num1)<<endl;

    cout<<"pow function: "<<pow(3, 5)<<endl;
    cout<<"sqrt function: "<<sqrt(49)<<endl;


    cout<<"log function: "<<log(33)<<endl;

    cout<<"sin function: "<<sin(30 * pi/180)<<endl;  // sin(30*pi/180) = sin 30

    return 0;
}
