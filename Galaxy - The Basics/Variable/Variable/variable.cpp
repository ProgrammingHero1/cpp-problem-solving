#include<iostream>
using namespace std;
int main()
{
    int x; //x is a variable which can store integer type data
    x = 10; //We simply give an integer value to x
    cout<<"The value of the integer is : "<<x<<"\n"; //Printing the value of x
    char ch; // ch is a variable which can store character type data
    ch='A'; //We simply give a character type value to ch
    cout<<"The value of the character is : "<<ch<<"\n"; //Printing the value of ch
    
    //So, in this program there are two variables. x and ch
    return 0;
}
