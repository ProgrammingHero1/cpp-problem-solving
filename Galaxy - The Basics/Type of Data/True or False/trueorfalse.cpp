#include<iostream>
using namespace std;
int main()
{
    //Boolean data type is used to store this two state: true, false
    //The declaration and definition is as same as other data type exccept character
    
    //declaring two boolean type variable
    bool first_statement;
    bool second_statement;
    
    //defining those two variable
    first_statement=true;
    second_statement=false;
    
    //now printing this two boolean variable
    
    cout<<"The first statement is : "<<first_statement<<"\n";
    cout<<"The second statement is : "<<second_statement<<"\n";
    
    return 0;
    
}
