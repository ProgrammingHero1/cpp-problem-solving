#include<iostream>
using namespace std;
int main()
{
    int i=1;
    
    /*
     Like for loop, while loop also has three part. Initialization, condition, increment/decrement. But, in while loop, initialization is needed to be
         done before the loop begins.
     */
    
    while(i<=5)
    {
        cout<<"I love Programming Hero\n";
        i++;
    }
    
    return 0;
}
