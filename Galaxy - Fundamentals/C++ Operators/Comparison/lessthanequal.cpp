#include<iostream>
using namespace std;
int main()
{
    int num1=9, num2=19;
    
    if(num1<=num2) //Here "<=" is a relational operator which is known as less than equal
    {
        cout<<"First number is less than or equal to the second number.\n";
    }
    else
    {
        cout<<"First number is greater than second number.\n";
    }
    
    return 0;
}
