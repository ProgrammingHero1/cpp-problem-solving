#include<iostream>
using namespace std;
int main()
{
    int num1=34, num2=43;
    
    if(num2>num1) //Here ">" is a relational operator which is known as greater than
    {
        cout<<"The second number is greater than the first number\n";
    }
    else
    {
        cout<<"The first number is greater than the second number\n";
    }
    
    return 0;
}
