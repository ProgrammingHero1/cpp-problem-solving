#include<iostream>
using namespace std;
int main()
{
    //Let's declare and define two integer variable named "num1" and "num2"
    
    int num1=5, num2=25;
    
    if(num1<num2) //Here "<" is a relational operator which is known as less than
    {
        cout<<"The first number of this program is less than the second number.\n";
    }
    else
    {
        cout<<"The first number of this program is greater than the second number.\n";
    }
    
    return 0;
}
